void solve()
{
    int n;
    cin >> n;

    int ans;
    for (int i = 0; i < n; i++)
    {
        int temp;
        cin >> temp;
        if (i == 0)
            ans = temp;
        else
            ans &= temp;
    }

    cout << ans << endl;
}

